const mongoose = require("mongoose");
const Schema = mongoose.Schema;

//==============================================================================
// BODY
//==============================================================================

const OfficeHours = new Schema({
  //Required
  uni: {
    type: String,
    required: true,
    text: true
  },
  tue: {
    type: String
  },
  wed: {
    type: String
  },
  thu: {
    type: String
  },
  fri: {
    type: String
  }
});

//==============================================================================
// !EXPORT
//==============================================================================

OfficeHours.index({
  uni: "text"
});
module.exports = officeHours = mongoose.model("OfficeHours", OfficeHours);
