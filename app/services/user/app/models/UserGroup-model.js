const mongoose = require("mongoose");
const Schema = mongoose.Schema;

//==============================================================================
// BODY
//==============================================================================

const UserGroupShema = new Schema({
  //Required
  groupID: {
    type: String,
    required: true
  },
  uni: {
    type: String,
    required: true,
    text: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

//==============================================================================
// !EXPORT
//==============================================================================

UserGroupShema.index({
  uni: "text"
});
module.exports = userGroup = mongoose.model("UserGroup", UserGroupShema);
